# 初心者歓迎！手と目で覚える正規表現入門・その１「さまざまな形式の電話番号を検索しよう」(@jnchito)
# https://qiita.com/jnchito/items/893c887fbf19e17d3ff9

text = <<-TEXT
名前：田中太郎
電話：03(1234)5678
？？：9999-99-9999
？？：03(1234-5678
住所：東京都世田谷区1111111ｰ111
TEXT
p numbers = text.scan(/0[1-9]\d{0,3}[-(]\d{1,4}[-)]\d{4}/)
# => ["03(1234)5678", "03(1234-5678"]
p numbers.grep(/\(\d+\)|-\d+-/)
# => ["03(1234)5678"]
